package bean;

public class HistorySearch {
	private String link;
	private String key;
	
	public HistorySearch() {}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}
	
	
}
